package playground;

import java.util.ArrayList;

public class ArrayListToArray {
    public static void main(String[] args) {
        String value = "[\"21a70210-a10e-11ec-a26d-efddb8d2f758\", \"219db340-a10e-11ec-a26d-efddb8d2f758\"]";
        ArrayList<String> valueList = new ArrayList<>();
        valueList.add("21a70210-a10e-11ec-a26d-efddb8d2f758");
        valueList.add("219db340-a10e-11ec-a26d-efddb8d2f758");
        System.out.println(value);
        System.out.println(valueList);
    }
}
